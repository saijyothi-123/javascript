// switch
// Dice Values: 1-6

const dice = 5;

// switch

switch (dice){
    case 1:
        console.log('you got 1');
        break;
    case 2:
        console.log("you got 2");
        break;
    case 3:
        console.log("you got 3");
        break;
    case 4:
        console.log("you got 4");
        break;
    case 5:
        console.log("you got 5");
        break;
    case 6:
        console.log("you got 6");
        break
    default:
        console.log("you didn't roll the dice");
}