// add strings, but strings will be concatenated:
let a="software"+ " " +"developer"
document.write(a)
/n


// If you put a number in quotes, the rest of the numbers will be treated as strings, and concatenated.
let b = "5" + 2 + 3;
document.write(b)
/n


// The result of adding 2 + 3 + "5" is:
let c = 2 + 3 + "5";
